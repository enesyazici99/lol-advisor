export function LoadingSpinner({ size = "md" }: { size?: "sm" | "md" | "lg" }) {
  const sizes = {
    sm: "w-5 h-5 border-2",
    md: "w-8 h-8 border-2",
    lg: "w-12 h-12 border-3",
  };

  return (
    <div className="flex items-center justify-center p-8">
      <div
        className={`${sizes[size]} rounded-full border-border border-t-accent animate-spin`}
      />
    </div>
  );
}
